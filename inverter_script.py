#!/usr/bin/env python
import os
import pandas as pd

exec(open("python.py").read())
module('load', 'apps/synopsys/hspice/F-2011.09-SP2')

from classes.ptm import ptm
from classes.subckts import subckts

def main():
    # For the given path, get the full path list of transistors models
    dir_name = os.getcwd() + '/modelfiles/'

    temp = ptm("test", "nfet.pm", "pfet.pm")

    models_subdirs = temp.set_fet_names(dir_name)
    models_list = temp.get_models_libs()
    subdir_paths = temp.set_uut_dir(dir_name) # create uut directories and sbudirs

    nfin = "1000m"

    timing_data = []
    delay_data = []
    power_data = []

    for model_subdir in models_subdirs:
        parts = model_subdir.split("/")
        parts.reverse()
        parts = "".join(parts)
        subuut = [subuut_name for subuut_name in models_list if parts in subuut_name]
        with open(os.getcwd() + "/" + temp.uut + "/" + subuut[0] + ".sp", 'w+') as uut:
            (fet_size, fet_voltage) = temp.get_fet_params(model_subdir)
            vdd_50 = str(float(fet_voltage) / 2)

            uut_size = 2
            serial_instance = 2
            load_amount = 1
            sim_time = str(2 ** uut_size)

            subckts_modules = subckts(uut, fet_size, nfin, fet_voltage)

            uut.write("$This UUT was automatically generated by:\n")
            uut.write("$" + os.getcwd() + "/" + __file__ + "\n\n")

            uut.write("$Netlist of " + temp.uut + "\n")
            uut.write(".lib '../models' " + subuut[0] + "\n\n")

            uut.write("$Sources\n")
            uut.write("vdd vdd  gnd " + fet_voltage + "V\n")
            subckts_modules.set_inverter()

            subckts_modules.write_source(uut_size)
 
            # inputs uut_size, load ammount, sim_time
            subckts_modules.write_inverter(uut_size, serial_instance)

            uut.write("$Output Loads\n")
            subckts_modules.write_outputs(uut_size, load_amount, serial_instance)

            subckts_modules.measure_delays(uut_size, serial_instance)
            subckts_modules.print_wave(uut_size, serial_instance)
            subckts_modules.measure_power(uut_size, sim_time, serial_instance)

            uut.write(".option post=2\n\n")
 
            uut.write("$Analysis\n")
            uut.write(".tran 10ps " + sim_time + "ns\n\n")

            uut.write(".measure tran source_power avg power\n")

            uut.write(".end")

        uut_script_dir = os.getcwd()
        os.chdir(temp.uut)
        os.system('hspice ' + subuut[0] + ".sp > " + subuut[0] + ".lis")
        os.chdir(uut_script_dir)

        timing_series = []
        delay_series = []
        power_series = []
        with open(os.getcwd() + "/" + temp.uut + "/" + subuut[0] + ".lis") as results:
            timing_flag = 0
            delay_flag = 0
            power_flag = 0
            for line in results:
                if ("x\n" in line):
                    timing_flag = 1
                elif ("y\n" in line) and (timing_flag == 1):
                    timing_flag = 0
                elif (timing_flag == 1):
                    timing_series.append(line.split())

                if ("transient analysis" in line):
                    delay_flag = 1
                elif ("x\n" in line) and (delay_flag == 1):
                    delay_flag = 0
                elif (delay_flag == 1):
                    delay_series.append(line.split())

                if ("avg_power" in line):
                    power_flag = 1
                    power_series.append(line.split())
                elif ("job concluded" in line) and (power_flag == 1):
                    power_flag = 0
                elif (power_flag == 1):
                    power_series.append(line.split())

        timing_data.append(timing_series)
        delay_data.append(delay_series)
        power_data.append(power_series)

#    print(timing_data[0][1])
#    print(timing_data[0][2][1])

    data = pd.DataFrame(timing_data[0], columns = ['Time', 'Voltage_in', 'Voltage_out'])
    data_no_indices = data.to_string(index=False)
    #print(data_no_indices)
    print(delay_data)
    print(power_data)

if __name__ == '__main__':
    main()

